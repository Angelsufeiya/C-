//标题:另类加法
//	请编写一个函数，将两个数字相加。不得使用 + 或其他算数运算符。
//
//	1, 2
//返回 : 3
//

#include <iostream>
using namespace std;

int main() {
	int A, B;
	cin >> A >> B;

	cout << A + B << endl;

	int res;
	if (A < B) {
		int tmp = A;
		A = B;
		B = tmp;
	}

	while (A) {
		if (A & 1) {
			res << 1;
		}
		A >> 1;
	}
	return 0;
}


